    FILE_FORMAT = (TYPE = CSV, SKIP_HEADER = 1, FIELD_OPTIONALLY_ENCLOSED_BY = '\"');

    -- Load data into MFG_SITE_RECOVERY
    COPY INTO MFG_SITE_RECOVERY 
    FROM @NATIVE_APP_QUICKSTART_DB.NATIVE_APP_QUICKSTART_SCHEMA.MY_INTERNAL_STAGE/site_recovery_data.csv.gz
    FILE_FORMAT = (TYPE = CSV, SKIP_HEADER = 1, FIELD_OPTIONALLY_ENCLOSED_BY = '\"');
